    public int buscarCurso(){
        int posicion=-1;
        String nombre=this.txtNombre.getText();
        for(int i=0;i<this.lstCursos.size();i++){
            if(nombre.equalsIgnoreCase(this.lstCursos.get(i).getNombre())){
                posicion=i;
                break;
            }
        }
        return posicion;
    }
