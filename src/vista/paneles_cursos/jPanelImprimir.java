    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int posicion=buscarCurso();
        String informacion="";
        
        if(posicion>-1){
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/YYY");
            String fecha = formato.format(this.lstCursos.get(posicion).getFecha().getTime());
            informacion = informacion + "=======INFORMACION DEL CURSO======="+
                    "\n"+"Nombre: "+this.lstCursos.get(posicion).getNombre()+
                    "\nSemestre: "+this.lstCursos.get(posicion).getSemestre()+
                    "\nFecha: "+fecha+"\n";
            
            for(int i=0;i<this.lstCursos.get(posicion).getLstModulos().size();i++){
                clsModulo auxModulo = this.lstCursos.get(posicion).getLstModulos().get(i);
                informacion = informacion +"\tModulos\n"+
                        "\tTema: "+auxModulo.getTema()+
                        "\t\nPorcentaje: "+auxModulo.getPorcentaje()+"%"+"\n";
            }
            this.txtPaneResultado.setText(informacion);
        }else{
            JOptionPane.showMessageDialog(this, "Curso no Encontrado!",
                    "Busqueda",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed


